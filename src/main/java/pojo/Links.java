package pojo;

public class Links {
	
	private String self;
	private String notes;
	private String award_emoji;
	private String project;
	private String closed_as_duplicate_of;
	
	public String getSelf() {
		return self;
	}
	public void setSelf(String self) {
		this.self = self;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
	public String getAward_emoji() {
		return award_emoji;
	}
	public void setAward_emoji(String award_emoji) {
		this.award_emoji = award_emoji;
	}
	public String getProject() {
		return project;
	}
	public void setProject(String project) {
		this.project = project;
	}
	public String getClosed_as_duplicate_of() {
		return closed_as_duplicate_of;
	}
	public void setClosed_as_duplicate_of(String closed_as_duplicate_of) {
		this.closed_as_duplicate_of = closed_as_duplicate_of;
	}



}
